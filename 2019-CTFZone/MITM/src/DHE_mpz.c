int get_primitive_root(mpz_t p, mpz_t g) {
    mpz_t power_1, power_2, res, buf;
    mpz_init(g);
    mpz_init(buf);
    mpz_init(res);
    mpz_init(power_2);
    mpz_init_set_ui(power_1, 2);
    mpz_sub_ui(buf, p, 1);
    mpz_div_ui(power_2, buf, 2);
    srand(time(NULL));
    for (;;) {
        mpz_set_ui(g, rand() % 20 + 3);
        mpz_nextprime(g, g);
        if (mpz_cmp(g, p) > 0) {
            continue;
        }
        mpz_powm(res, g, power_1, p);
        if (mpz_cmp_ui(res, 1) != 0) {
            mpz_powm(res, g, power_2, p);
            if (mpz_cmp_ui(res, 1) != 0) {
                break;
            }
        }
    } 
    mpz_clear(buf); 
    mpz_clear(res);
    mpz_clear(power_1);
    mpz_clear(power_2); 
}

int8_t is_prime(mpz_t p, int8_t k) {
    mpz_t buf, s, r, x, j;
    gmp_randstate_t state;
    unsigned long int seed;
    
    if ((mpz_cmp_ui(p, 2) == 0)|| (mpz_cmp_ui(p, 3) == 0)) {
        return 1;
    } 
    if (!mpz_tstbit(p, 0)) {
        return 0;
    }
    mpz_init(x);
    mpz_init(r);
    mpz_init_set_ui(s, 0);
    mpz_init(buf);
    mpz_init(j);
    gmp_randinit_default(state);
    seed = time(NULL);
    gmp_randseed_ui(state, seed);
    mpz_sub_ui(r, p, 1);
    for(;;) {
        mpz_mod_ui(buf, r, 2);
        if (mpz_cmp_ui(buf, 0)) {
            break;
        }
        mpz_add_ui(s, s, 1);
        mpz_div_ui(r, r, 2);
    }
    for(int8_t i = 0; i < k; i++) {
        mpz_sub_ui(buf, p, 4);
        mpz_urandomm(buf, state, buf);
        mpz_add_ui(buf, buf, 2);
        mpz_powm(x, buf, r, p);
        mpz_sub_ui(buf, p, 1);
        mpz_set_ui(j, 1);
        mpz_set_ui(r, 2);
        if (mpz_cmp_ui(x, 1) == 0 || mpz_cmp(x, buf) == 0) {
            mpz_clear(buf);
            mpz_clear(s);
            mpz_clear(r);
            mpz_clear(x);
            mpz_clear(j);
            gmp_randclear(state);
            return 1;
        } 
        while (mpz_cmp(j, s) != 0) {
            mpz_powm(x, x, r, p);
            if (mpz_cmp_ui(x, 1) == 0) {
                mpz_clear(buf);
                mpz_clear(s);
                mpz_clear(r);
                mpz_clear(x);
                mpz_clear(j);
                gmp_randclear(state);
                return 0;
            }
            if (mpz_cmp(x, buf) == 0) {
                mpz_clear(buf);
                mpz_clear(s);
                mpz_clear(r);
                mpz_clear(x);
                mpz_clear(j);
                gmp_randclear(state);
                return 1;
            }
            mpz_add_ui(j, j, 1);
        }
        mpz_clear(buf);
        mpz_clear(s);
        mpz_clear(r);
        mpz_clear(x);
        mpz_clear(j);
        gmp_randclear(state);
        return 0;
    }
    mpz_clear(buf);
    mpz_clear(s);
    mpz_clear(r);
    mpz_clear(x);
    mpz_clear(j);
    gmp_randclear(state);
    return 1;
}

int8_t is_prime_safe_candidate(mpz_t p) {
    mpz_t buf, prime, r;
    mpz_init(prime);
    mpz_init(buf);
    mpz_init(r);
    for(int16_t i = 0; i < 2000; i++) {
        mpz_set_ui(prime, primes[i]); 
        if (mpz_cmp(prime, p) >= 0) {
            break;
        }
        mpz_set_ui(r, res[i]); 
        mpz_mod(buf, p, prime);
        if (mpz_cmp(buf, r) != 0 && mpz_cmp_ui(buf, 0) != 0) {
            continue;
        }
        else {
            mpz_clear(r);
            mpz_clear(prime);
            mpz_clear(buf);
            return 0;
        }
    }
    mpz_clear(r);
    mpz_clear(prime);
    mpz_clear(buf);
    return 1;
}

