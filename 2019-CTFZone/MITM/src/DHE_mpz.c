static void generate_private_key(mpz_t priv_key) {
    gmp_randstate_t state;
    uint32_t seed;
    seed = time(NULL);
    gmp_randinit_default(state);
    gmp_randseed_ui(state, seed);
    mpz_urandomb(priv_key, state, 349);
    mpz_setbit(priv_key, 349);  
    gmp_randclear(state);
}

static void generate_public_key(mpz_t pub_key, mpz_t g, mpz_t priv_key, mpz_t p) {
    mpz_powm(pub_key, g, priv_key, p);
}

void DHE_generate_symmetric_key(mpz_t key, mpz_t pub_key, mpz_t priv_key, mpz_t p) {
    mpz_powm(key, pub_key, priv_key, p); 
}

int8_t DHE_generate_parameters(mpz_t p, mpz_t g, uint16_t bits_length) {
    if (!get_safe_prime_number(p, bits_length)) {
        return 0;
    }
    get_primitive_root(p, g);
    return 1;
}

void DHE_generate_private_and_public_key(mpz_t private_key, mpz_t public_key, mpz_t p, mpz_t g) {
    generate_private_key(private_key);
    generate_public_key(public_key, g, private_key, p);
}
